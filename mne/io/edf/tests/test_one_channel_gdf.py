# the file in question doesn't have highpass and lowpass
@pytest.mark.filterwarnings('ignore::RuntimeWarning')
def test_one_channel_gdf():
    """Test line endings of mne-python."""
    tempdir = _TempDir()
    fname = op.join(tempdir, 'single_channel.gdf')
    with open(fname, 'wb') as fid:
        fid.write(one_channel_gdf())
    # pytest.raises(AssertionError, _assert_line_endings, tempdir)
    # now check mne
    ecg = mne.io.read_raw_gdf(fname, preload=True)
    assert ecg['ECG'][0].shape == (1, 4500)
    assert 150.0 == ecg.info['sfreq']


